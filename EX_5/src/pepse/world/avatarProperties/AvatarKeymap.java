package pepse.world.avatarProperties;

public class AvatarKeymap {

    public int moveRight;
    public int moveLeft;
    public int jump;
    public int fly;


    public AvatarKeymap (int moveRight, int moveLeft, int jump, int fly){
        this.moveRight = moveRight;
        this.moveLeft = moveLeft;
        this.jump = jump;
        this.fly = fly;
    }
}
